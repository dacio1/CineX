    private void BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarActionPerformed
        // TODO add your handling code here:

        Ator ator = new Ator();
        AtorDAO AtorDAO = new AtorDAO();
        List<Ator> ListaDeAtores = AtorDAO.ListarAtores();
        int codigoexcluir = Integer.parseInt(CampoCodigo.getText());
        DefaultTableModel tabela = new DefaultTableModel();
        tabela = (DefaultTableModel) Tabela.getModel();
        tabela.setRowCount(0);
        for (int i = 0; i < ListaDeAtores.size(); i++) {
            ator = ListaDeAtores.get(i);
            if(ListaDeAtores.get(i).getCodigo() == codigoexcluir){

                Object[] objeto = {ListaDeAtores.get(i).getCodigo(),
                    ListaDeAtores.get(i).getNome(),
                    ListaDeAtores.get(i).getNacionalidade()};

                tabela.addRow(objeto);
            }
        }

    }//GEN-LAST:event_BuscarActionPerformed

