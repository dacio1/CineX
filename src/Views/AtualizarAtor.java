    private void AtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AtualizarActionPerformed
        // TODO add your handling code here:
        Ator ator = new Ator();
        //AtorDAO AD = new AtorDAO();
        AtorController ac = new AtorController();
        List<Ator> ListaDeAtores = ac.listar();

        int codigoatualizar = Integer.parseInt(CampoCodigo.getText());

        for (int i = 0; i < ListaDeAtores.size(); i++) {

            ator = ListaDeAtores.get(i);
            if(ListaDeAtores.get(i).getCodigo() == (codigoatualizar)){
                ator.setCodigo(codigoatualizar);

                try{
                    //Ator temp = new Ator( CampoNome.getText(), CampoNacionalidade.getText());
                    //temp.setCodigo(codigoatualizar);
                    //AD.AtualizaAtor(temp);
                    ac.atualizar(codigoatualizar, CampoNome.getText(), CampoNacionalidade.getText());
                    JOptionPane.showMessageDialog(null, "Ator atualizado com sucesso!");

                } catch (ErroInternoException ex) {
                    Logger.getLogger(AtualizarAtor.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(null, "Ocorreu um erro!");
                } catch (SQLException ex) {
                    Logger.getLogger(AtualizarAtor.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(null, "Ocorreu um erro!");
                }
            }
        }
        
        CampoCodigo.setText("");
        CampoNome.setText("");
        CampoNacionalidade.setText("");
        DefaultTableModel tabela = new DefaultTableModel();
        tabela = (DefaultTableModel) Tabela.getModel();
        tabela.setNumRows(0);
    }//GEN-LAST:event_AtualizarActionPerformed

