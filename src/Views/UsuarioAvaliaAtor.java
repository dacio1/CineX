    private void AvaliarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AvaliarActionPerformed
        // TODO add your handling code here:
        
          AtorDAO AD = new AtorDAO();
        int codigo = Integer.parseInt( CampoCodigoUsuario.getText());
        int codigoAtor = Integer.parseInt(CampoCodigoAtor.getText());
        double pontuacao = Double.parseDouble(Pontuacao.getText());
        
        try{
        AD.AvaliaAtor(new Ator(codigo , codigoAtor, pontuacao ));
        
        JOptionPane.showMessageDialog(null, "Ator avaliado com sucesso!");
        }catch(ErroInternoException e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Ocorreu um erro ou esse ator j치 foi avaliado por vc ou ele n칚o existe");
        }
    }//GEN-LAST:event_AvaliarActionPerformed

