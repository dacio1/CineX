    private void ListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ListarActionPerformed
        // TODO add your handling code here:
         AtorDAO AD = new AtorDAO();
         List<Ator> ListaDeAtores = AD.ListarAtores();
                 
         DefaultTableModel tabela = new DefaultTableModel();
         tabela = (DefaultTableModel) TabelaAtor.getModel();
         tabela.setRowCount(0);
         for (int i = 0; i < ListaDeAtores.size(); i++) {
            Object[] objeto = {ListaDeAtores.get(i).getCodigo(),
                               ListaDeAtores.get(i).getNome(),
                               ListaDeAtores.get(i).getNacionalidade()};
                tabela.addRow(objeto);
            }
    }//GEN-LAST:event_ListarActionPerformed

